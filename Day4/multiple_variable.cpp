#include<bits/stdc++.h>
using namespace std;

int main()
{
    int x = 4, y = 10, z = 6; // Multiple variable declaration in a single line
    cout << "Sum of three numbers: " << x + y + z << endl;

    // one value to multiple variables
    int a, b, c;
    a = b = c = 50;
    cout << x + y + z << endl;
    return 0;
}