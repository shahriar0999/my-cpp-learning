#include <bits/stdc++.h>
using namespace std;

int main() {
    int i = 1;
    while (i <= 10) {
        cout << i << endl;
        i++;  //incrementing the value of i by 1
    }

    int j = 3;
    while (j < 0){
        cout << j << endl;
        j--; //decrementing the value of j by 1
    }

    return 0;
}